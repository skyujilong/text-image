def get_scheme(key: str | None) -> NarrationScheme:
    """按 key 取方案；未知/None 回退默认方案（不抛错，保证下游有可用模板）。"""
    if key and key in NARRATION_SCHEMES:
        return NARRATION_SCHEMES[key]
    return NARRATION_SCHEMES[DEFAULT_SCHEME_KEY]


def default_templates(key: str | None) -> dict[str, str]:
    """取某方案的默认模板对（供前端预填、后端兜底）。"""
    scheme = get_scheme(key)
    return {
        "adapt_script": scheme.adapt_script_template,
        "scene_change": scheme.scene_change_template,
    }


def list_scheme_presets() -> list[dict[str, str]]:
    """序列化全部内置方案（含默认模板正文），供 interrupt payload 下发前端。"""
    return [
        {
            "key": s.key,
            "label": s.label,
            "description": s.description,
            "adapt_script_template": s.adapt_script_template,
            "scene_change_template": s.scene_change_template,
        }
        for s in NARRATION_SCHEMES.values()
    ]


class NarrationTemplateError(ValueError):
    """run 内解说模板非法（缺字段/缺必需占位符）。"""


def validate_templates(templates: object) -> dict[str, str]:
    """校验并归一 run 内自定义模板，返回 {adapt_script, scene_change}。

    - templates 必须是 dict，两个字段均为非空字符串。
    - 每个字段必须保留其必需占位符（否则模型拿不到关键输入）——用户编辑删掉了就判非法。
    非法时抛 NarrationTemplateError（与其它节点显式抛错风格一致）。
    """
    if not isinstance(templates, dict):
        raise NarrationTemplateError(f"narration_templates 必须为对象，实际: {type(templates).__name__}")
    out: dict[str, str] = {}
    for field, required in (
        ("adapt_script", ADAPT_SCRIPT_REQUIRED_TOKENS),
        ("scene_change", SCENE_CHANGE_REQUIRED_TOKENS),
    ):
        val = templates.get(field)
        if not isinstance(val, str) or not val.strip():
            raise NarrationTemplateError(f"narration_templates.{field} 必须为非空字符串")
        missing = [tok for tok in required if tok not in val]
        if missing:
            raise NarrationTemplateError(
                f"narration_templates.{field} 缺少必需占位符 {missing}，无法喂给模型"
            )
        out[field] = val
    return out
