# ── 模板渲染 ──────────────────────────────────────────────────────────────


def render_template(template: str, context: dict[str, str]) -> str:
    """把 `%%KEY%%` 占位替换为 context[KEY]。

    用纯字符串替换而非 str.format：prompt 正文里的 JSON 花括号 `{}` 无需转义，
    用户随意编辑模板也不会因引入 `{}` 而崩。context 的 key 不含 `%%` 包裹。
    """
    out = template
    for key, value in context.items():
        out = out.replace(f"%%{key}%%", value)
    return out


